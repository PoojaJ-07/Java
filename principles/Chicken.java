package com.program.principles;

public class Chicken {


    public String chickenBreed;
    public int chickenAge;
    public boolean isRooster;
    public double chickenWeight;
    public String chickenColor;
    public int eggsLaid;
    public double wingSpan;
    public String chickenName;
    public boolean isFreeRange;
    public double dailyFeedAmount;

    public Chicken() {
        System.out.println("Created constructor chicken using no arguments");
    }

    // 10 methods
    public void cluck() {
        System.out.println("The chicken is clucking.");
    }

    public void peck() {
        System.out.println("The chicken is pecking.");
    }

    public void layEgg() {
        System.out.println("The chicken laid an egg.");

    }

    public void run() {
        System.out.println("The chicken is running.");
    }

    public void flapWings() {
        System.out.println("The chicken is flapping its wings.");
    }

    public void sleep() {
        System.out.println("The chicken is sleeping.");
    }

    public void eat() {
        System.out.println("The chicken is eating.");
    }

    public void drink() {
        System.out.println("The chicken is drinking water.");
    }

    public void strut() {
        System.out.println("The chicken is strutting.");
    }

    public void crow() {
        System.out.println("The chicken is crowing.");
    }
}

